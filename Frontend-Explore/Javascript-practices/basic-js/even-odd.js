// even number 

let num = 1;
while(num<=10){
    console.log(num);


    if(num%2 ===0){

        console.log('even number:', num);

    }
    num++;
}




// odd number 

let number = 1;
while(number<=10){
    console.log(number);


    if(number%2 !==0){

        console.log('odd number:', number);

    }
    number++;
}


